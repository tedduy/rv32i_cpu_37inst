# =============================================================================
# Test Case 1.1.1: ADD Instruction
# =============================================================================
# Description: Verify ADD rd, rs1, rs2 instruction
# Category: ISA Coverage - R-Type Instructions
# Priority: CRITICAL
# =============================================================================

.section .text
.globl _start

_start:
    # =========================================================================
    # Test 1: Basic ADD - x1 = x2 + x3
    # =========================================================================
    li      x2, 16              # x2 = 16 (0x10)
    li      x3, 32              # x3 = 32 (0x20)
    add     x1, x2, x3          # x1 = x2 + x3 = 48 (0x30)
    
    # =========================================================================
    # Test 2: ADD with zero - x4 = x5 + x0
    # =========================================================================
    li      x5, 0xDEADBEEF      # x5 = 0xDEADBEEF
    add     x4, x5, x0          # x4 = x5 + 0 = 0xDEADBEEF
    
    # =========================================================================
    # Test 3: ADD with overflow - x6 = x7 + x8
    # =========================================================================
    li      x7, 0xFFFFFFFF      # x7 = -1 (or 0xFFFFFFFF)
    li      x8, 1               # x8 = 1
    add     x6, x7, x8          # x6 = 0xFFFFFFFF + 1 = 0 (wraps around)
    
    # =========================================================================
    # Test 4: ADD negative numbers - x9 = x10 + x11
    # =========================================================================
    li      x10, -16            # x10 = -16 (0xFFFFFFF0)
    li      x11, -32            # x11 = -32 (0xFFFFFFE0)
    add     x9, x10, x11        # x9 = -16 + (-32) = -48 (0xFFFFFFD0)
    
    # =========================================================================
    # Test 5: ADD with same source registers - x12 = x13 + x13
    # =========================================================================
    li      x13, 100            # x13 = 100
    add     x12, x13, x13       # x12 = 100 + 100 = 200
    
    # =========================================================================
    # End of test
    # =========================================================================
    j       _end

_end:
    # Infinite loop to stop simulation
    j       _end

